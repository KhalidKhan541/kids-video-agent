def get_rhyme_names() -> list[str]:
    return list(RHYME_TEMPLATES.keys())


def get_rhyme(name: str) -> dict | None:
    return RHYME_TEMPLATES.get(name)


def search_rhymes(query: str) -> list[tuple[str, dict]]:
    q = query.lower()
    results = []
    for name, data in RHYME_TEMPLATES.items():
        if q in name.lower() or any(q in t.lower() for t in data.get("topics", [])):
            results.append((name, data))
    return results
